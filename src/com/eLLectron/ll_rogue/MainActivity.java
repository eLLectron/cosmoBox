	public void reroll(View v) {
		tileMap.removeAllViews();
		generate(tileMap);
		disp.displayMap(tileMap, map, player.y, player.x, this);
	}

	public void generate(GridLayout tileMap) {
		gen.clearMap(map);
		gen.gen(map, player);

	}

	public void left(View v) {
		if (player.x > 9 && map[player.y][player.x - 1] == 1)//
			player.x -= 1;
		player.orientation = 0;
		disp.refresh(map, tileMap, player, this);
	}

	public void up(View v) {
		if (player.y > 6 && map[player.y - 1][player.x] == 1)//
			player.y -= 1;
		player.orientation = 1;
		disp.refresh(map, tileMap, player, this);
	}

	public void down(View v) {
		if (player.y < 44 && map[player.y + 1][player.x] == 1)//
			player.y += 1;
		player.orientation = 2;
		disp.refresh(map, tileMap, player, this);
	}

	public void right(View v) {
		if (player.x < 39 && map[player.y][player.x + 1] == 1)//
			player.x += 1;
		player.orientation = 3;
		disp.refresh(map, tileMap, player, this);
	}
